class Model:
    def sum(self, num1, num2):
        # переход в реализацию метода СЛОЖЕНИЯ
        return num1 + num2

    def min(self, num1, num2):
        # переход в реализацию метода ВЫЧИТАНИЯ
        return num1 - num2

    def mult(self, num1, num2):
        # переход в реализацию метода УМНОЖЕНИЯ
        return num1 * num2

    def div(self, num1, num2):
        # переход в реализацию метода ДЕЛЕНИЯ
        return num1 / num2
